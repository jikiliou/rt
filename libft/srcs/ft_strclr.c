#include "libft.h"

void	ft_strclr(char *s)
{
	int i;
	int	len;

	if (s)
	{
		len = ft_strlen(s);
		i = 0;
		while (i < len)
		{
			s[i] = '\0';
			i++;
		}
	}
}
